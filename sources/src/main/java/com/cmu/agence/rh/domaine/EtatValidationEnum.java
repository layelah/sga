package com.cmu.agence.rh.domaine;

public enum EtatValidationEnum {
    
    
    Encours("EN_COURS"),
    Acceptee("ACCEPTE"),
    Refusee("REFUSE");
    
    
    private String code;
    
    private EtatValidationEnum(String code) {
        this.code = code;
    }
    
    @Override
    public String toString() {
        return code;
    }
    
    public String getCode() {
        return code;
    }
    
}
