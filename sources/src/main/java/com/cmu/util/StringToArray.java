package com.cmu.util;

/**
 *
 * @author babacar
 */
public final class StringToArray {

    public static String getPageByURL(String url) {

        String values[] = url.split("/");
        String s = "";
        for (String i : values) {
            s = i;
        }
        return s;
    }
}
