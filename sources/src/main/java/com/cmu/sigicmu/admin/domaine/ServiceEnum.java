package com.cmu.sigicmu.admin.domaine;

public enum ServiceEnum {
    DirectionGeneral("DG"),
    CSI("CSI");
       
    private String code;

    private ServiceEnum(String code) {
        this.code = code;
    }

    public String getNiveau() {
        return this.code;
    }
    
    @Override
    public String toString() {
        return code;
    }
    
    public String getCode() {
        return code;
    } 
}
